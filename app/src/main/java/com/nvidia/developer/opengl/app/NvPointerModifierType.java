package com.nvidia.developer.opengl.app;

/**
 * Pointer event key modifiers (generally mouse-only).
 * @author Nvidia 2014-9-12 17:41
 */
public class NvPointerModifierType {

	/** Shift key down */
	public static final int SHITFT = 0x00000001;
	/** Control key down */
	public static final int CTRL = 0x00000002;
	/** Alt key down */
	public static final int ALT = 0x00000004;
}
